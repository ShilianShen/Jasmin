// CREATE & DELETE =====================================================================================================
LOTRI_MW* LOTRI_DeleteMW(LOTRI_MW *mw) {
    if (mw == NULL) return mw;

    mw->model = LOTRI_DeleteModel(mw->model);
    mw->world = LOTRI_DeleteWorld(mw->world);
    free(mw); mw = NULL;

    return NULL;
}



